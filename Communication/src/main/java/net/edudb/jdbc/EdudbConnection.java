    @Override
    public void close() throws SQLException {
        if (isClosed) {
            throw new SQLException("Connection is already closed");
        }
        isClosed = true;
        try {
            client.closeConnection();
        } catch (IOException e) {
            throw new RuntimeException(e);
        }
    }

